package wonyong.by.movierecommend;

public class MovieRankRecyclerData {
    String title;
    String openDate;
    String totalSales;
    String totalAudience;
    String rank;

    public MovieRankRecyclerData(String title, String openDate, String totalSales, String totalAudience, String rank){
        this.title = title;
        this.openDate = openDate;
        this.totalSales = totalSales;
        this.totalAudience = totalAudience;
        this.rank = rank;
    }



}
